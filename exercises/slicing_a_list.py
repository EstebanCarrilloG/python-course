'''
Print out the slice ['b', 'c', 'd'] of the letters list using slicing.
'''
letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
print(letters[1:4])

'''
Print out the slice ['a', 'b', 'c'] of the letters list using slicing.
'''
letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
print(letters[:3])
'''
Print out the slice ['e', 'f', 'g'] of the letters list using slicing.
'''
letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
print(letters[4:])